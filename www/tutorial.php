<h2>Introduction</h2>
<p>This tutorial will show you how to create a simple graphical demo
using Rage. As Rage hides the details of the graphics system, you can
very easily get some graphics on the screen, without learning anything
about tilemap offsets or VRAM mappings.</p>

<h2>Installing Rage</h2>
* download rage-1.tar.bz2<br>
* unzip<br>
* make install<br>

<h2>Starting out</h2>
* make new project<br>
* edit makefile<br>
* include graphics in gfx/<br>
* write grit files<br>

<h2>Entering code</h2>
* rename main.c to main.cpp<br>
* add #include "rage.h" and Rage rage;<br>


